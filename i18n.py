def normalize_language(language: object) -> str:
    if isinstance(language, str) and language in TEXT:
        return language
    return "en"


def language_code_from_name(name: str) -> str:
    for code, language_name in LANGUAGE_NAMES.items():
        if name == language_name:
            return code
    return "en"


def normalize_color_theme(theme: object) -> str:
    if isinstance(theme, str) and theme in COLOR_THEME_NAMES["en"]:
        return theme
    return "sky_blue"


def color_theme_code_from_name(language: str, name: str) -> str:
    for code, theme_name in COLOR_THEME_NAMES[normalize_language(language)].items():
        if name == theme_name:
            return code
    return "sky_blue"
